# 6시 25분 시작 Python : 100000000, C# 10000000000 # 7시 56분 종료
SELECT ID, EMAIL, FIRST_NAME, LAST_NAME
FROM DEVELOPERS
WHERE (SUBSTR(ROUND(BIN(SKILL_CODE)/(SELECT BIN(CODE) FROM SKILLCODES WHERE NAME = 'Python'), 0), -1, 1) = 1)
OR
(SUBSTR(ROUND(BIN(SKILL_CODE)/(SELECT BIN(CODE) FROM SKILLCODES WHERE NAME = 'C#'), 0), -1, 1) = 1)
ORDER BY 1